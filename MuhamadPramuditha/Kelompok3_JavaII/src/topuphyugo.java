    private void btnAksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAksiActionPerformed
    // Ambil data transaksi
    String region = jComboBox1.getSelectedItem() != null ? jComboBox1.getSelectedItem().toString() : "";
    String diamond = jList1.getSelectedValue() != null ? jList1.getSelectedValue() : "";
    String id = jTextField1.getText();
    String server = jTextField2.getText();
    String metodePembayaran = "";

    if (jRadioButton1.isSelected()) metodePembayaran = "Dana";
    else if (jRadioButton2.isSelected()) metodePembayaran = "ShopeePay";
    else if (jRadioButton3.isSelected()) metodePembayaran = "Via Bank";
    else if (jRadioButton4.isSelected()) metodePembayaran = "MiniMarket";

    // Ambil pilihan rank yang dipilih
    String rank = "";
    if (epic.isSelected()) rank = "Epic - Legend";
    else if (legend.isSelected()) rank = "Legend - Mythic";
    else if (mythic.isSelected()) rank = "Mythic - Mythic Honor";
    else if (mythichonor.isSelected()) rank = "Mythic Honor - Immortal";

    // Pastikan semua data sudah diisi sebelum lanjut
    if (region.isEmpty() || diamond.isEmpty() || id.isEmpty() || server.isEmpty() || metodePembayaran.isEmpty() ) {
        JOptionPane.showMessageDialog(null, "Harap isi semua informasi sebelum menyetujui transaksi!");
        return;
    }

    // Format data transaksi dengan rank yang dipilih
    String transaksi = "Transaksi Joki Mobile Legends\n" +
                       "ID: " + id + "\n" +
                       "Server: " + server + "\n" +
                       "Region: " + region + "\n" +
                       "Diamond: " + diamond + "\n" +
                       "Joki Rank: " + rank + "\n" +
                       "Metode Pembayaran: " + metodePembayaran + "\n";

    // Masukkan teks ke jTextArea1
    jTextArea1.append(transaksi + "\n\n");
    
    simpanTransaksi(id, server, region, diamond, rank, metodePembayaran);
   
    //teks sukses
    successTextLabel = new JLabel("Yeayyyy Pesanan berhasil!");
    successTextLabel.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 17));
    successTextLabel.setBounds(10, 10, 300, 340);
    
    
    ImageIcon imageIcon = new ImageIcon(getClass().getResource("/yay.gif"));
    JLabel imageLabel = new JLabel(imageIcon); // Menambahkan gambar ke JLabel

    // Menambahkan gambar pada panel Riwayat Transaksi
    imageLabel.setBounds(-5, -23, imageIcon.getIconWidth(), imageIcon.getIconHeight());
    jgmbr.add(successTextLabel);
    jgmbr.add(imageLabel);
    jgmbr.setLayout(null); // Agar kita bisa bebas tempatkan komponen
    
    jgmbr.repaint();

    
    // Aktifkan tab Riwayat Transaksi jika belum aktif
    jTabbedPane1.setEnabledAt(2, true);

    // Pindah ke tab Riwayat Transaksi
    jTabbedPane1.setSelectedIndex(2); 
    }//GEN-LAST:event_btnAksiActionPerformed
    
    private void simpanTransaksi(String id, String server, String region, String diamond, String rank, String metodePembayaran) {
    try {
        Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/topupml", "topupml", "123");

        String sql = "INSERT INTO TOPUPML (ID, SERVER, REGION, DIAMOND, RANK, METODE_PEMBAYARAN) VALUES (?, ?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, id);
        pst.setString(2, server);
        pst.setString(3, region);
        pst.setString(4, diamond);
        pst.setString(5, rank);
        pst.setString(6, metodePembayaran);

        pst.executeUpdate();
        conn.close();
        
        System.out.println("Transaksi berhasil disimpan!");

    } catch (SQLException e) {
        e.printStackTrace();
    }
}

