    private void cetaklaporanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetaklaporanActionPerformed
       try {
        // 1. Koneksi ke database
        Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/topupml", "topupml", "123");

        // 2. Compile file jrxml yang ada di folder "report"
        JasperReport report = JasperCompileManager.compileReport(
            getClass().getResourceAsStream("/report/transaksiReport.jrxml")
        );

        // 3. Isi laporan tanpa parameter (null), ambil dari database
        JasperPrint print = JasperFillManager.fillReport(report, null, conn);

        // 4. Tampilkan laporan
        JasperViewer.viewReport(print, false);
        
        // Ekspor ke PDF
        JasperExportManager.exportReportToPdfFile(print, "laporan.pdf");


    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal mencetak laporan: " + e.getMessage());
        e.printStackTrace();
    }

    }//GEN-LAST:event_cetaklaporanActionPerformed

